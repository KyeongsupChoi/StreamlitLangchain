# Default for regions not in OFFICIAL_PRICE_TABLE, by year.
DEFAULT_BY_YEAR: dict[int, int] = {
    2019: 1_100_000,
    2020: 1_300_000,
    2021: 1_600_000,
    2022: 1_500_000,
    2023: 1_700_000,
    2024: 2_000_000,
}

LATEST_YEAR = 2024

# Prefix match order: longest prefix first so specific districts win over
# province-level fallbacks (e.g. "서울 강남구" beats "서울").
_PREFIX_ORDER: list[str] = sorted(
    OFFICIAL_PRICE_TABLE.keys(), key=len, reverse=True
)


def lookup(region: str, year: int | None = None) -> int:
    """
    Return official land price per ㎡ (KRW) for the given region and year.

    Params:
        region: Administrative region (e.g. "서울 강남구", "경기 성남시").
        year: Reference year (2019-2024). Defaults to LATEST_YEAR.

    Returns:
        Official land price in KRW per ㎡.
    """
    year = year if year is not None else LATEST_YEAR
    year = max(2019, min(LATEST_YEAR, year))  # clamp to available range
    region = (region or "").strip()
    for prefix in _PREFIX_ORDER:
        if region.startswith(prefix):
            year_table = OFFICIAL_PRICE_TABLE[prefix]
            return year_table.get(year, year_table[LATEST_YEAR])
    return DEFAULT_BY_YEAR.get(year, DEFAULT_BY_YEAR[LATEST_YEAR])
